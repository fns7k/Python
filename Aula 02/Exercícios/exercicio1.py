#Ler um número inteiro e apresentar o quadrado do número

numero= int(input('Digite um número para saber o seu quadrado:'))       #Solicitar número para o quadrado do mesmo
print(f'O quadrado do número {numero} é: {numero*numero}')      #Mostrar resultado no terminal


