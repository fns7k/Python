#Faça um programa que peça para que o usuario digitar duas notas, exibir a média

print(40*'-')       #Imprimir 40 "-"
nota1= int(input('Digite a sua primeira nota: '))       #Solicitar a primeira nota do usuário
nota2= int(input('Digite a sua segunda nota: '))       #Solicitar a segunda nota do usuário
print(40*'-')       #Criar uma linha com 40 "-"
print(f'A média das notas é {(nota1+nota2)/2}')        #Mostrar o resultado no terminal