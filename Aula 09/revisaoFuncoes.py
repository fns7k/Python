import math  # Importa o módulo 'math' que contém funções matemáticas como sqrt (raiz quadrada).

def raizQuadrada(numero):
    resultadoRaiz = math.sqrt(numero)  # Calcula a raiz quadrada do número usando math.sqrt().
    return resultadoRaiz  # Retorna o resultado da raiz quadrada.


def mediaNotas(nota1, nota2):
    media = (nota1 + nota2) / 2  # Calcula a média aritmética de dois números.
    return media  # Retorna a média.

